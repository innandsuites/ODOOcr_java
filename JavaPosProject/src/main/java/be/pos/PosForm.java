    public void pagar() {
        list_producto = new ArrayList<Producto>();
        //rendimiento = new Rendimiento();
        if (rendimientodb.existencia()) {
            rendimientodb.actualizar(rendimiento);
        }
        if (!rendimientodb.existencia()) {
            rendimientodb.insertar(rendimiento);
        }
        while (modelo.getRowCount() > 0) {
            producto = new Producto();
            str_copy = modelo.getValueAt(0, 0).toString();
            producto.setId(str_copy);
            str_copy = modelo.getValueAt(0, 2).toString();
            int_copy = Integer.valueOf(str_copy);
            producto.setCantidad(int_copy);
            list_producto.add(producto);
            modelo.removeRow(0);
        }
        productodb.actualizarCantidad(list_producto);
        rendimiento = new Rendimiento();
        suma_total = 0.0;
        jTextFieldTot.setText("");
        jTextFieldCod.requestFocus();
    }

    public void nuevaVenta() {
        jfrmvuelto.darVuelto(suma_total);
        pagar();
        jfrmvuelto.limpiarCampos();
        jfrmvuelto.setVisible(true);
//        jfrmvuelto.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
    }

