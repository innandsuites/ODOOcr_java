    private void jButtonAgrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgrActionPerformed

        var_consulta = jTextFieldCod.getText();
        producto = new Producto();
        //rendimiento = new Rendimiento();
        if (jTextFieldCod.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Falta valor de Codigo");
        } else {
            try {

                producto = productodb.consultar(var_consulta);
                if (producto.getId() == "") {
                    JOptionPane.showMessageDialog(null, "Producto no registrado");
//                        jfrmagregar.setVisible(true);
                } else {
                    registry_count = modelo.getRowCount();
                    rendimiento.setIngreso(rendimiento.getIngreso() + (Integer.valueOf(jTextFieldCan.getText())) * producto.getGanancia());
                    rendimiento.setImpuesto(rendimiento.getImpuesto() + (Integer.valueOf(jTextFieldCan.getText())) *producto.getIva());
                    rendimiento.setVenta(rendimiento.getVenta() + (Integer.valueOf(jTextFieldCan.getText())) *producto.getPrecio());

                    datos[0] = producto.getId();
                    datos[1] = producto.getNombre();
                    datos[2] = jTextFieldCan.getText();

                    double_copy = Double.parseDouble(jTextFieldCan.getText()) * producto.getPrecio();
                    str_copy = String.format("%.02f",double_copy);
                    datos[3] = str_copy;
//                    modelo.addRow(datos);
                    modelo.insertRow(0,datos);
                    //cadena_total += Integer.toString(registry_count);
//                    cadena_total = modelo.getValueAt(registry_count, 3).toString();
                    cadena_total = modelo.getValueAt(0, 3).toString();
                    suma_total += Double.valueOf(cadena_total);
                    jTextFieldTot.setText(String.format("%.02f",suma_total));
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error " + e);
            }
        }
        jTextFieldCod.setText("");
        jTextFieldCan.setText("1");
        jTextFieldCod.requestFocus();
    }//GEN-LAST:event_jButtonAgrActionPerformed

    private void jButtonEliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliActionPerformed
        // TODO add your handling code here:
        producto = new Producto();

        int filaSelect = jTablePos.getSelectedRow();
        if (filaSelect >= 0) {
            //cadena_total = modelo.getValueAt(filaSelect, 3).toString();
            var_consulta = modelo.getValueAt(filaSelect, 0).toString();
            producto = productodb.consultar(var_consulta);
            rendimiento.setIngreso(rendimiento.getIngreso() - (Integer.valueOf(modelo.getValueAt(filaSelect, 2).toString()) * producto.getGanancia()));
            rendimiento.setImpuesto(rendimiento.getImpuesto() - (Integer.valueOf(modelo.getValueAt(filaSelect, 2).toString()) * producto.getIva()));
            rendimiento.setVenta(rendimiento.getVenta() - (Integer.valueOf(modelo.getValueAt(filaSelect, 2).toString()) * (producto.getPrecio())));
            //suma_total -= Integer.valueOf(cadena_total);
            suma_total -= (Integer.valueOf(modelo.getValueAt(filaSelect, 2).toString()) * (producto.getPrecio()));
            jTextFieldTot.setText(String.valueOf(suma_total));
            modelo.removeRow(filaSelect);

        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        }

        jTextFieldCod.requestFocus();
    }//GEN-LAST:event_jButtonEliActionPerformed

