    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String filename = "error.xml";
        cred = new Credencial();
        if (jComboBox1.getSelectedIndex() == 0) {
            cred.setAmbiente("Produccion");
            filename = "prod.xml";
        }
        if (jComboBox1.getSelectedIndex() == 1) {
            cred.setAmbiente("Pruebas");
            filename = "sandbox.xml";
        }
        cred.setUsuario(jTextField1.getText());
        cred.setClave(jTextField2.getText());
        cred.setClientid(jTextField3.getText());
        cred.setApi(jTextField4.getText());
        cred.setOauth(jTextArea1.getText());
        XMLOutputFactory xmlof
                = XMLOutputFactory.newFactory();
        FileWriter fw = null;
        try {
            fw = new FileWriter(filename);
        } catch (IOException ex) {
            Logger.getLogger(IdpPnl.class.getName()).log(Level.SEVERE, null, ex);
        }
        XMLStreamWriter xmlsw = null;
        try {
            xmlsw = xmlof.createXMLStreamWriter(fw);
            xmlsw.writeStartDocument();
            xmlsw.writeStartElement("Credencial");
            xmlsw.writeStartElement("Ambiente");
            xmlsw.writeAttribute("id", cred.getAmbiente());
            xmlsw.writeStartElement("Usuario");
            xmlsw.writeCharacters(cred.getUsuario());
            xmlsw.writeEndElement();
            xmlsw.writeStartElement("Clave");
            xmlsw.writeCharacters(cred.getClave());
            xmlsw.writeEndElement();
            xmlsw.writeStartElement("Api");
            xmlsw.writeCharacters(cred.getApi());
            xmlsw.writeEndElement();
            xmlsw.writeStartElement("Oauth");
            xmlsw.writeCharacters(cred.getOauth());
            xmlsw.writeEndElement();
            xmlsw.writeStartElement("Clientid");
            xmlsw.writeCharacters(cred.getClientid());
            xmlsw.writeEndElement();
            xmlsw.writeEndElement();
            xmlsw.writeEndElement();
            xmlsw.writeEndDocument();
            xmlsw.flush();
            xmlsw.close();
            jLabel1.setText("Guardado..!");

        } catch (XMLStreamException ex) {
            Logger.getLogger(IdpPnl.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error" + ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String filename = "error.xml";
        cred = new Credencial();
        if (jComboBox1.getSelectedIndex() == 0) {
            filename = "prod.xml";
        }
        if (jComboBox1.getSelectedIndex() == 1) {
            filename = "sandbox.xml";
        }

        DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();
        try {
            dbf.setFeature(XMLConstants.FEATURE_SECURE_PROCESSING, true);

            // parse XML file
            DocumentBuilder db = dbf.newDocumentBuilder();

            Document doc = db.parse(new File(filename));

            doc.getDocumentElement().normalize();

            // get <staff>
            NodeList list = doc.getElementsByTagName("Ambiente");
            for (int temp = 0; temp < list.getLength(); temp++) {

                Node node = list.item(temp);

                if (node.getNodeType() == Node.ELEMENT_NODE) {

                    Element element = (Element) node;

                    // get staff's attribute
                    cred.setAmbiente(element.getAttribute("id"));

                    // get text
                    cred.setUsuario(element.getElementsByTagName("Usuario").item(0).getTextContent());
                    cred.setClave(element.getElementsByTagName("Clave").item(0).getTextContent());
                    cred.setApi(element.getElementsByTagName("Api").item(0).getTextContent());
                    cred.setOauth(element.getElementsByTagName("Oauth").item(0).getTextContent());
                    cred.setClientid(element.getElementsByTagName("Clientid").item(0).getTextContent());
                    
                }
                jTextField1.setText(cred.getUsuario());
                jTextField2.setText(cred.getClave());
                jTextField3.setText(cred.getClientid());
                jTextField4.setText(cred.getApi());
                jTextArea1.setText(cred.getOauth());
            }

        } catch (ParserConfigurationException ex) {
            Logger.getLogger(IdpPnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SAXException ex) {
            Logger.getLogger(IdpPnl.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(IdpPnl.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jComboBox1ActionPerformed

